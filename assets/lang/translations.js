// Variabel untuk bahasa aktif saat ini
let currentLanguage = 'id'; // default Bahasa Indonesia

// Authentication system
const users = {
    'admin': { password: 'admin123', role: 'admin', name: 'Dr. Maria Santos' },
    'user1': { password: 'user123', role: 'user', name: 'User Demo' }
};

let currentUser = null;

// Fungsi untuk mengubah bahasa
function changeLanguage(lang) {
    currentLanguage = lang;
    
    // Update semua elemen yang memiliki atribut data-lang
    const elements = document.querySelectorAll('[data-lang]');
    elements.forEach(element => {
        const key = element.getAttribute('data-lang');
        if (translations[lang] && translations[lang][key]) {
            // Untuk input placeholder khusus login
            if (element.id === 'username' || element.id === 'password') {
                element.placeholder = translations[lang][key];
            }
            // Untuk input placeholder umum
            else if (element.tagName === 'INPUT' && element.type !== 'submit') {
                element.placeholder = translations[lang][key];
            }
            // Untuk option value
            else if (element.tagName === 'OPTION') {
                element.textContent = translations[lang][key];
            }
            // Untuk teks biasa
            else {
                element.textContent = translations[lang][key];
            }
        }
    });
    
    // Update tombol bahasa aktif
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.remove('active');
    });
    document.querySelector(`[onclick="changeLanguage('${lang}')"]`).classList.add('active');
    
    // Simpan pilihan bahasa di localStorage
    localStorage.setItem('selectedLanguage', lang);
}

// Fungsi untuk memuat bahasa tersimpan saat halaman dimuat
function loadSavedLanguage() {
    const savedLang = localStorage.getItem('selectedLanguage');
    if (savedLang && translations[savedLang]) {
        changeLanguage(savedLang);
    }
}

// Fungsi untuk simulasi submit form (dummy)
function submitForm(formId) {
    const form = document.getElementById(formId);
    if (form) {
        // Validasi sederhana
        const inputs = form.querySelectorAll('input[required], select[required], textarea[required]');
        let isValid = true;
        
        inputs.forEach(input => {
            if (!input.value.trim()) {
                input.style.borderColor = '#ff6b6b';
                isValid = false;
            } else {
                input.style.borderColor = '#A8D5BA';
            }
        });
        
        if (isValid) {
            alert(translations[currentLanguage].formSubmitted);
            form.reset();
        }
    }
    return false; // Prevent actual form submission
}

// Fungsi untuk simulasi login (dummy)
function attemptLogin() {
    const username = document.getElementById('username').value.trim();
    const password = document.getElementById('password').value.trim();
    
    // Check credentials
    if (users[username] && users[username].password === password) {
        // Store user session
        const userData = {
            username: username,
            role: users[username].role,
            name: users[username].name,
            loginTime: new Date().toISOString()
        };
        
        localStorage.setItem('currentUser', JSON.stringify(userData));
        currentUser = userData;
        
        // Redirect based on role
        if (userData.role === 'admin') {
            window.location.href = 'dashboard.html';
        } else {
            window.location.href = 'user-dashboard.html';
        }
        
        return true;
    } else {
        alert(translations[currentLanguage].invalidLogin);
        return false;
    }
}

// Check if user is logged in
function checkAuth() {
    const userData = localStorage.getItem('currentUser');
    if (userData) {
        currentUser = JSON.parse(userData);
        return true;
    }
    return false;
}

// Check if user has specific role
function hasRole(requiredRole) {
    if (!currentUser) {
        const userData = localStorage.getItem('currentUser');
        if (userData) {
            currentUser = JSON.parse(userData);
        } else {
            return false;
        }
    }
    return currentUser && currentUser.role === requiredRole;
}

// Logout function
function logout() {
    localStorage.removeItem('currentUser');
    currentUser = null;
    window.location.href = 'login.html';
}

// Protect admin pages
function protectAdminPage() {
    if (!hasRole('admin')) {
        alert('Akses ditolak! Halaman ini hanya untuk administrator.');
        window.location.href = 'login.html';
        return false;
    }
    return true;
}

// Protect user pages
function protectUserPage() {
    if (!checkAuth()) {
        alert('Silakan login terlebih dahulu untuk mengakses halaman ini.');
        window.location.href = 'login.html';
        return false;
    }
    return true;
}

// Redirect if already logged in
function redirectIfLoggedIn() {
    if (checkAuth()) {
        if (currentUser.role === 'admin') {
            window.location.href = 'dashboard.html';
        } else {
            window.location.href = 'user-dashboard.html';
        }
    }
}

